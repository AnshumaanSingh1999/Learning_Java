public class foreachwalaloop {
    public static void main(String[] args) {
        String names[]={"Shubh", "Rinku", "Somya", "Anshu"};
        for(String a:names){
            System.out.println(a);
        }  
        int l=names.length;
        for(int i=0;i<l;i++){
            System.out.println(names[i]);
        } 
    }
}
