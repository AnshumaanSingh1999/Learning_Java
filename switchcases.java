public class  switchcases{
    public static void main(String[] args) {
        int x=2;
        switch(x){
            case 1:
            System.out.println("this is 1");
            break;
            case 6:
            System.out.println("This is 6");
            break;
            default:
            System.out.println("Ye to hona hi tha");
            break;
        }   
    }
}
